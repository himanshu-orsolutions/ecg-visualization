package application.models;

/**
 * The model Amplitude. It holds all the information of an ECG amplitude.
 */
public class Amplitude {

	private Double i;
	private Double ii;
	private Double iii;
	private Double aVR;
	private Double aVL;
	private Double aVF;
	private Double v1;
	private Double v2;
	private Double v3;
	private Double v4;
	private Double v5;
	private Double v6;

	public Double getI() {
		return i;
	}

	public void setI(Double i) {
		this.i = i;
	}

	public Double getIi() {
		return ii;
	}

	public void setIi(Double ii) {
		this.ii = ii;
	}

	public Double getIii() {
		return iii;
	}

	public void setIii(Double iii) {
		this.iii = iii;
	}

	public Double getaVR() {
		return aVR;
	}

	public void setaVR(Double aVR) {
		this.aVR = aVR;
	}

	public Double getaVL() {
		return aVL;
	}

	public void setaVL(Double aVL) {
		this.aVL = aVL;
	}

	public Double getaVF() {
		return aVF;
	}

	public void setaVF(Double aVF) {
		this.aVF = aVF;
	}

	public Double getV1() {
		return v1;
	}

	public void setV1(Double v1) {
		this.v1 = v1;
	}

	public Double getV2() {
		return v2;
	}

	public void setV2(Double v2) {
		this.v2 = v2;
	}

	public Double getV3() {
		return v3;
	}

	public void setV3(Double v3) {
		this.v3 = v3;
	}

	public Double getV4() {
		return v4;
	}

	public void setV4(Double v4) {
		this.v4 = v4;
	}

	public Double getV5() {
		return v5;
	}

	public void setV5(Double v5) {
		this.v5 = v5;
	}

	public Double getV6() {
		return v6;
	}

	public void setV6(Double v6) {
		this.v6 = v6;
	}
}
